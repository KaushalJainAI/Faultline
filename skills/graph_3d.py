class Graph3DGenerator:
    """Converts enriched ASTGrapher output into a self-contained Dash app script."""

    def generate(self, graph_data: dict, output_path: str) -> str:
        """
        Build the node/link lists, compute layouts, write the Dash app script.
        Returns the path to the generated .py file.
        """
        nodes, links = self._build(graph_data)

        pos_full = _compute_layout(nodes, links, mode="full")
        pos_func = _compute_layout(nodes, links, mode="func")

        sep = ","
        script = _TEMPLATE
        script = script.replace("__NODES__",    json.dumps(nodes,    separators=(sep, ":")))
        script = script.replace("__LINKS__",    json.dumps(links,    separators=(sep, ":")))
        script = script.replace("__POS_FULL__", json.dumps(pos_full, separators=(sep, ":")))
        script = script.replace("__POS_FUNC__", json.dumps(pos_func, separators=(sep, ":")))

        # Always output as a .py file
        out = Path(output_path)
        if out.suffix.lower() != ".py":
            out = out.with_suffix(".py")
        out.parent.mkdir(parents=True, exist_ok=True)
        out.write_text(script, encoding="utf-8")
        return str(out)

    # ─────────────────────────────────────────────────────────────────────
    # Build node + link lists from ASTGrapher output
    # ─────────────────────────────────────────────────────────────────────

    def _build(self, graph_data: dict):
        nodes: list[dict] = []
        links: list[dict] = []
        node_ids: set[str] = set()
        link_keys: set[tuple] = set()

        def add_node(n: dict):
            if n["id"] not in node_ids:
                node_ids.add(n["id"])
                nodes.append(n)

        def add_link(src: str, tgt: str, ltype: str):
            key = (src, tgt, ltype)
            if key not in link_keys and src in node_ids and tgt in node_ids:
                link_keys.add(key)
                links.append({"source": src, "target": tgt, "type": ltype})

        sigs = graph_data.get("signatures", {})

        ser_map: dict[str, list] = {}
        for s in (graph_data.get("serializer_schemas") or []):
            ser_map[s["class_id"]] = s["fields"]

        for fp, fi in graph_data.get("files", {}).items():
            if isinstance(fi, dict) and fi.get("error"):
                continue

            file_id = f"file:{fp}"
            add_node({"id": file_id, "type": "file",
                       "label": Path(fp).name, "path": fp,
                       "group": str(Path(fp).parent)})

            for cls in (fi.get("classes") or []):
                cls_id  = f"class:{fp}:{cls['name']}"
                node_d: dict = {
                    "id": cls_id, "type": "class",
                    "label": cls["name"], "path": fp,
                    "lineno": cls.get("lineno"), "group": fp,
                }
                if cls_id in ser_map:
                    node_d["serializer_fields"] = ser_map[cls_id]
                add_node(node_d)
                add_link(file_id, cls_id, "contains")

                for method in (cls.get("methods") or []):
                    mid = f"method:{fp}:{cls['name']}.{method}"
                    sig = sigs.get(mid, {})
                    add_node({"id": mid, "type": "method",
                               "label": method, "path": fp,
                               "group": f"{fp}:{cls['name']}",
                               "sig": sig.get("sig_str", "")})
                    add_link(cls_id, mid, "contains")

            for fn in (fi.get("functions") or []):
                fid = f"func:{fp}:{fn['name']}"
                sig = sigs.get(fid, {})
                add_node({"id": fid, "type": "function",
                           "label": fn["name"], "path": fp,
                           "lineno": fn.get("lineno"), "group": fp,
                           "sig": sig.get("sig_str", "")})
                add_link(file_id, fid, "contains")

        for src, tgt in (graph_data.get("dependencies") or []):
            add_link(f"file:{src}", f"file:{tgt}", "imports")

        for e in (graph_data.get("call_edges") or []):
            add_link(e["source"], e["target"], "calls")

        for e in (graph_data.get("inheritance_edges") or []):
            add_link(e["source"], e["target"], "inherits")

        return nodes, links
