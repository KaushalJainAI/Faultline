def _compute_layout(nodes: list, links: list, mode: str = "full", seed: int = 42) -> dict:
    """
    Returns {node_id: [x, y, z]} using networkx spring layout.
    Falls back to spherical random layout if networkx is unavailable.
    """
    import math

    if mode == "func":
        func_types = {"class", "function", "method"}
        vis_ids = {n["id"] for n in nodes if n["type"] in func_types}
        edge_pairs = [
            (l["source"], l["target"])
            for l in links
            if l["type"] in ("calls", "inherits")
            and l["source"] in vis_ids
            and l["target"] in vis_ids
        ]
        node_ids = list(vis_ids)
    else:
        node_ids = [n["id"] for n in nodes]
        vis_ids = set(node_ids)
        edge_pairs = [(l["source"], l["target"]) for l in links]

    if not node_ids:
        return {}

    try:
        import networkx as nx

        G = nx.Graph()
        G.add_nodes_from(node_ids)
        G.add_edges_from(
            (s, t) for s, t in edge_pairs if s in vis_ids and t in vis_ids
        )
        # Cap iterations for large graphs
        iters = 20 if len(G.nodes) > 300 else 40
        k_val = max(1.0, 2.0 / max(1, len(G.nodes) ** 0.3))
        pos = nx.spring_layout(G, k=k_val, iterations=iters, seed=seed, dim=3)
        scale = 20.0
        return {nid: [float(c) * scale for c in coords] for nid, coords in pos.items()}
    except ImportError:
        pass
    except Exception:
        pass

    # Fallback: distribute uniformly on a sphere
    rng = random.Random(seed)
    result = {}
    for i, nid in enumerate(node_ids):
        phi = math.acos(1 - 2 * (i + 0.5) / max(len(node_ids), 1))
        theta = math.pi * (1 + 5 ** 0.5) * i
        r = 20.0
        result[nid] = [
            r * math.sin(phi) * math.cos(theta),
            r * math.sin(phi) * math.sin(theta),
            r * math.cos(phi),
        ]
    return result


